    public static boolean existepunto(String cadena){
    boolean resultado;
    resultado = false;
    for (int i=0; i< cadena.length(); i++){
    
        if (cadena.substring(i, i+1).equals(".")){
        resultado= true;
        break;
        }
    }    
    return resultado;
}

