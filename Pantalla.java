    private void jbtotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtotalActionPerformed
        String resultado;
        memoria2=textpantalla.getText();
       
        if (!memoria2.equals(" ")){
           resultado=calculadora(memoria1,memoria2,operando);
           textpantalla.setText(resultado);
       }
    }
    
    
public static String  calculadora(String memoria1, String memoria2,String operando){    
        Double resultado=0.0;
        String respuesta;

        if (operando.equals("+")){
        resultado=Double.parseDouble(memoria1)+ Double.parseDouble(memoria2);
        }
        if (operando.equals("-")){
            resultado=Double.parseDouble(memoria1)- Double.parseDouble(memoria2);
        }
        if (operando.equals("*")){
            resultado=Double.parseDouble(memoria1)* Double.parseDouble(memoria2);
        }
        if (operando.equals("/")){
        resultado=Double.parseDouble(memoria1)/ Double.parseDouble(memoria2);
        }
        respuesta=resultado.toString();
        return respuesta;        
        
    }//GEN-LAST:event_jbtotalActionPerformed

